%%%-------------------------------------------------------------------
%%% @doc
%%%        关于时间的方法
%%% @end
%%%-------------------------------------------------------------------
-module(gb_time_lib).
-author("lihuachao").

%% API
-export([now_sec/0,now_mili/0,now_us/0]).

%%%-------------------------------------------------------------------
%%% @doc
%%%     得到当前秒
%%% @end
%%%-------------------------------------------------------------------
-spec now_sec() -> integer().
now_sec() ->
    {M, S, _} = os:timestamp(),
    M * 1000000 + S.

%%%-------------------------------------------------------------------
%%% @doc
%%%     得到当前毫秒
%%% @end
%%%-------------------------------------------------------------------
-spec now_mili() -> integer().
now_mili() ->
    {M, S, Ms} = os:timestamp(),
    M * 1000000000 + S*1000 + Ms div 1000.

%%%-------------------------------------------------------------------
%%% @doc
%%%     得到当前微妙
%%% @end
%%%-------------------------------------------------------------------
-spec now_us() -> integer().
now_us() ->
    {M, S, Ms} = os:timestamp(),
    M * 1000000000000 + S*1000000 + Ms.